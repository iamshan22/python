# Example 1: create tuple
t1 = 10
print(t1 ,type(t1)) # <class 'int'>

t2 = 10,
print(t2 ,type(t2)) # <class 'tuple'>

t3 = 10,20,30
print(t3 ,type(t3)) # <class 'tuple'>

t4 = (10,20,30)
print(t4 ,type(t4)) # <class 'tuple'>